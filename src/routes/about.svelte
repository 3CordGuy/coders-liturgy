<article class="px-4 prose lg:prose-lg prose-slate dark:prose-invert mb-12">
	<h1 class="text-rose-600" id="about-page">About Coder's Liturgy</h1>
	<p>
		Software Development is not easy. If we believe that the power to do this alone is solely
		&quot;within us,&quot; we deceive ourselves. We wouldn't be where we are without help along the
		way. We need others. We need God&#39;s hand guiding us.
	</p>
	<blockquote class="border-l-4 pl-4 md:pl-12 text-lg">
		"If any of you lacks wisdom, let him ask God, who gives generously to all without reproach, and
		it will be given him” <cite class="text-sm">(James 1:5)</cite>.
	</blockquote>
	<p>
		Work is an order of "sub-creation". We image God when we work and create. God created
		"ex-nihilo" (out of nothing). Coding sometimes feels this way, like creating something from
		nothing. We start with a blank editor, but we stand on the shoulders of those who came before.
		Those who helped us along our path. We should give thanks for them.
	</p>
	<p>
		Our motives and heart behind our work can make all the difference between good work and just
		another task. This website aims to provide some meditative prayers and scripture as a means of
		heart redirecting, aligning our thoughts towards God's word and what is true at the end of the
		day.
	</p>
	<p>
		Truthfully, I built this for myself. To assuage the fears, to subdue the grumbling, and to point
		my heart in a better direction. When my heart displays selfishness and pride, these words are
		reminders written for myself. They are joined with what I believe are true, God-breathed
		scripture. This work is inspired by a couple volumes of beautiful prayers called "Every Moment Holy." 
		I just found myself wishing there were writings that were more relatable to me as a creator and coder.
	</p>
	<p>
		It&#39;s a bit of a scary thing to bare your heart—and to do it online. But I believe I&#39;m
		not alone in this. I felt compelled to make this public. Maybe these prayers and thoughts will
		encourage you as well, my friend.
	</p>
