	</p>
	<p>My body would rather rest. My mind would sooner stray from this task.</p>
	<p>
		As I reflect on this, I think of how my heart is prone to wander from you. I confess I would
		sometimes rather not put in the effort, but simply take the inheritance and run.
	</p>
	<p>But you are gracious and merciful. You welcome me like a patient, loving Father.</p>
	<p>By this same grace and mercy, I humbly ask you to remind me that:</p>
	<p>
		My work is ultimately for you — not people. My identity is in Jesus — not the work of my hands.
		You make straight my path.
	</p>
	<p>Lord be my motivation and turn my perspective on this task.</p>
	<p>Be the wellspring of my joy.</p>
	<p>May the overflow of my relationship with you spill out onto this good work.</p>
	<p>And may I trust you in all of it.</p>
	<p>Amen.</p>
